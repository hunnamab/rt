#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!n)
		return (1);
	if (s1 && s2)
	{
		while (*s1++ == *s2++ && n-- > 1)
		{
			if (*s1 == '\0' && *s2 == '\0')
				return (1);
			if (n == 1 && *s1 == *s2)
				return (1);
		}
	}
	return (0);
}
