#include "libft.h"

int		ft_strinv(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i])
	{
		if (s1[i] == s2[j])
			while (s1[i] == s2[j])
			{
				if (s2[j] == '\0')
					return (1);
				i++;
				j++;
			}
		j = 0;
		i++;
	}
	return (0);
}
